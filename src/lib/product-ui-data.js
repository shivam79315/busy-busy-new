const sanitizeId = (value) => value.toLowerCase().replace(/\s+/g, "-").replace(/[^a-z0-9-]/g, "");

export const enrichProductForUi = (product) => {
  const meta = PRODUCT_LIST_META[product.id] || DEFAULT_META_BY_CATEGORY[product.category] || {
    brand: "CoreLine",
    discount: 10,
    tags: ["everyday"],
  };

  return {
    ...product,
    brand: meta.brand,
    discount: meta.discount,
    tags: meta.tags,
  };
};

export const getProductDetailDemo = (product) => {
  const enrichedProduct = enrichProductForUi(product);
  const galleryPool = CATEGORY_GALLERY_IMAGES[product.category] || [];
  const uniqueGallery = [product.image, ...galleryPool].filter((image, index, arr) => arr.indexOf(image) === index);
  const variantGroups = CATEGORY_VARIANTS[product.category] || [];
  const specifications = CATEGORY_SPECIFICATIONS[product.category] || [];
  const technicalDetails = CATEGORY_TECH_DETAILS[product.category] || [];
  const faqs = CATEGORY_FAQS[product.category] || [];
  const reviews = CATEGORY_REVIEWS[product.category] || [];

  const reviewCount = 127 + enrichedProduct.discount;

  return {
    ...enrichedProduct,
    detailId: sanitizeId(product.id),
    galleryImages: uniqueGallery,
    variantGroups,
    detailedDescription: `${product.description} Crafted with a refined design language, this product blends premium finish, usability, and long-term comfort for everyday routines. Built for users who want functional performance without compromising on visual elegance.`,
    specifications,
    technicalDetails,
    faqs,
    reviews,
    reviewSummary: {
      average: product.rating,
      totalCount: reviewCount,
      recommendationPercent: Math.min(99, 82 + Math.floor(product.rating * 3)),
      breakdown: [
        { stars: 5, percent: Math.min(92, 62 + Math.floor(product.rating * 6)) },
        { stars: 4, percent: 22 },
        { stars: 3, percent: 10 },
        { stars: 2, percent: 4 },
        { stars: 1, percent: 2 },
      ],
    },
  };
};
